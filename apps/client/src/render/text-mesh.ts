const FONT_5X7 = FONT_5X7_BASE

export const CHAR_WIDTH = 5
export const CHAR_HEIGHT = 7
export const CHAR_SPACING = 1

export const measureTextWidth = (text: string, scale: number): number =>
  text.length === 0 ? 0 : (text.length * CHAR_WIDTH + (text.length - 1) * CHAR_SPACING) * scale

export const measureTextHeight = (scale: number): number => CHAR_HEIGHT * scale

const pushQuad = (
  vertices: number[],
  indices: number[],
  baseIndex: number,
  x: number,
  y: number,
  width: number,
  height: number,
  color: readonly [number, number, number, number?],
): void => {
  const [red, green, blue, alpha = 1] = color
  vertices.push(
    x,
    y,
    red,
    green,
    blue,
    alpha,
    x + width,
    y,
    red,
    green,
    blue,
    alpha,
    x + width,
    y + height,
    red,
    green,
    blue,
    alpha,
    x,
    y + height,
    red,
    green,
    blue,
    alpha,
  )
  indices.push(baseIndex, baseIndex + 1, baseIndex + 2, baseIndex, baseIndex + 2, baseIndex + 3)
}

export const buildTextMesh = (
  text: string,
  x: number,
  y: number,
  scale: number,
  color: readonly [number, number, number, number?],
): { vertexData: Float32Array; indexData: Uint32Array; indexCount: number } => {
  const vertices: number[] = []
  const indices: number[] = []
  let baseIndex = 0
  let cursorX = x

  for (const rawCharacter of text) {
    const glyph = FONT_5X7[rawCharacter] ?? FONT_5X7[' ']

    for (let row = 0; row < glyph.length; row += 1) {
      for (let col = 0; col < glyph[row].length; col += 1) {
        if (glyph[row][col] !== '1') {
          continue
        }

        pushQuad(
          vertices,
          indices,
          baseIndex,
          cursorX + col * scale,
          y + row * scale,
          scale,
          scale,
          color,
        )
        baseIndex += 4
      }
    }

    cursorX += (CHAR_WIDTH + CHAR_SPACING) * scale
  }

  return {
    vertexData: new Float32Array(vertices),
    indexData: new Uint32Array(indices),
    indexCount: indices.length,
  }
}
